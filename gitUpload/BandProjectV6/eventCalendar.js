function returnEvents(year){
  year = Number(year);
  eventArray = new Array();
  for(let i= 0; i<eventsWorking.length;i++){
    date = eventsWorking[i].date.split("-");
    if(Number(date[2]) == year){
      eventArray.push(eventsWorking[i]);
    }
  }

  return eventArray;
}
